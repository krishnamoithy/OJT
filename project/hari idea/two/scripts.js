// Function to show media
function showMedia() {
    const mainContent = document.getElementById('main-content');
    mainContent.innerHTML = `
        <h2>Media</h2>
        <div class="grid-container">
            ${mediaData.map((video, index) => `
                <div class="grid-item">
                    <video id="video-${index}" width="320" height="240" controls>
                        <source src="${video.src}" type="video/mp4">
                        Your browser does not support the video tag.
                    </video>
                    <h3>${video.title}</h3>
                    <p>${video.description}</p>
                </div>
            `).join('')}
        </div>
        <div class="controls">
            <button onclick="playPreviousVideo()">Previous</button>
            <button onclick="playNextVideo()">Next</button>
        </div>
    `;

    let currentVideoIndex = 0;
    const videos = mainContent.querySelectorAll('video');

    // Event listener for stopping all other videos when one video starts playing
    videos.forEach(video => {
        video.addEventListener('play', function() {
            const currentVideo = this;
            videos.forEach(v => {
                if (v !== currentVideo) {
                    v.pause();
                }
            });
        });
    });

    // Function to play the next video
    function playNextVideo() {
        if (currentVideoIndex < videos.length - 1) {
            currentVideoIndex++;
            playVideoAtIndex(currentVideoIndex);
        }
    }

    // Function to play the previous video
    function playPreviousVideo() {
        if (currentVideoIndex > 0) {
            currentVideoIndex--;
            playVideoAtIndex(currentVideoIndex);
        }
    }

    // Function to play video at a specific index
    function playVideoAtIndex(index) {
        videos.forEach((video, idx) => {
            if (idx === index) {
                video.play();
            } else {
                video.pause();
                video.currentTime = 0;
            }
        });
    }
}

